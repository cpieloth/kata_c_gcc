/**
 * Adds a and b.
 *
 * \param a summand
 * \param b summand
 * \return sum of a+b
 */
int add(int a, int b);

/**
 * Calculates the remainder of division of a by b.
 *
 * \param a dividend
 * \param b divisor
 * \return remainder of a/b
 */
int modulo(int a, int b);
